export function matchBirthPlace(ocrText: string | null): { name: string; code: string; score: number } | null {
  if (!ocrText) return null;
  const norm = normalize(ocrText).replace(/^(KOTA|KAB\.?|KABUPATEN)\s+/i, "").trim();
  if (!norm || norm.length < 3) return null;

  // Exact alias match
  const aliased = BIRTH_PLACE_ALIASES[norm];
  if (aliased) {
    const found = Object.entries(KABUPATEN).find(([, n]) => n === aliased);
    if (found) return { name: found[1], code: found[0], score: 1.0 };
  }

  const entries = getAllKabupatenNames();
  const threshold = norm.length <= 6 ? 0.72 : 0.65;
  let best: { name: string; code: string; score: number } | null = null;

  for (const entry of entries) {
    const s = similarity(norm, entry.normalized);
    if (s > threshold && (!best || s > best.score)) {
      best = { name: entry.name, code: entry.code, score: s };
    }
    // Substring containment — "INDRAGIRI HILIR" contains "HILIR"
    if (norm.length >= 4 && (norm.includes(entry.normalized) || entry.normalized.includes(norm))) {
      const cs = Math.max(s, 0.82);
      if (!best || cs > best.score) {
        best = { name: entry.name, code: entry.code, score: cs };
      }
    }
  }
  return best;
}

export function matchProvince(ocrText: string | null): { name: string; code: string; score: number } | null {
  if (!ocrText) return null;
  const norm = normalize(ocrText);
  if (!norm || norm.length < 3) return null;

  const aliasCode = PROVINCE_ALIASES[norm];
  if (aliasCode) return { name: PROVINCES[aliasCode], code: aliasCode, score: 1.0 };

  const threshold = norm.length <= 5 ? 0.75 : 0.6;
  let best: { name: string; code: string; score: number } | null = null;
  for (const [code, name] of Object.entries(PROVINCES)) {
    const normName = normalize(name);
    const s = similarity(norm, normName);
    if (s > threshold && (!best || s > best.score)) {
      best = { name, code, score: s };
    }
    if (norm.length >= 4 && (norm.includes(normName) || normName.includes(norm))) {
      const containScore = Math.max(s, 0.85);
      if (!best || containScore > best.score) {
        best = { name, code, score: containScore };
      }
    }
  }
  return best;
}

export function matchKabupaten(
  ocrText: string | null,
  provCode?: string | null
): { name: string; code: string; score: number } | null {
  if (!ocrText) return null;
  const norm = normalize(ocrText);
  if (!norm || norm.length < 3) return null;

  const threshold = norm.length <= 5 ? 0.75 : 0.6;
  let best: { name: string; code: string; score: number } | null = null;
  for (const [code, name] of Object.entries(KABUPATEN)) {
    if (provCode && !code.startsWith(provCode)) continue;
    const normName = normalize(name);
    const s = similarity(norm, normName);
    const bonus = provCode && code.startsWith(provCode) ? 0.05 : 0;
    const total = s + bonus;
    if (total > threshold && (!best || total > best.score)) {
      best = { name, code, score: total };
    }
    if (norm.length >= 4 && (norm.includes(normName) || normName.includes(norm))) {
      const containScore = Math.max(total, 0.85);
      if (!best || containScore > best.score) {
        best = { name, code, score: containScore };
      }
    }
  }
  return best;
}

export function getAllKabupatenForProvince(provCode: string): { code: string; name: string }[] {
  return Object.entries(KABUPATEN)
    .filter(([code]) => code.startsWith(provCode))
    .map(([code, name]) => ({ code, name }));
}

export function resolveRegions(data: {
  nik?: string | null;
  province?: string | null;
  city?: string | null;
  kecamatan?: string | null;
  kelurahan?: string | null;
}): {
  province: string | null;
  city: string | null;
  provinceCode: string | null;
  kabupatenCode: string | null;
} {
  let provinceCode: string | null = null;
  let kabupatenCode: string | null = null;
  let provinceName: string | null = null;
  let kabupatenName: string | null = null;

  if (data.nik && /^\d{16}$/.test(data.nik)) {
    provinceCode = lookupProvinceCodeByNik(data.nik);
    kabupatenCode = lookupKabupatenCodeByNik(data.nik);
    if (provinceCode) provinceName = PROVINCES[provinceCode];
    if (kabupatenCode) kabupatenName = KABUPATEN[kabupatenCode];
  }

  if (!provinceName && data.province) {
    const match = matchProvince(data.province);
    if (match) {
      provinceName = match.name;
      provinceCode = match.code;
    }
  }

  if (!kabupatenName && data.city) {
    const match = matchKabupaten(data.city, provinceCode);
    if (match) {
      kabupatenName = match.name;
      kabupatenCode = match.code;
      if (!provinceCode) {
        provinceCode = match.code.substring(0, 2);
        provinceName = PROVINCES[provinceCode] ?? null;
      }
    }
  }

  return {
    province: provinceName,
    city: kabupatenName,
    provinceCode,
    kabupatenCode,
  };
}
